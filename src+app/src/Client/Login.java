    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int port = Integer.parseInt(this.jTextField3.getText());
        int clientport = Integer.parseInt(this.jTextField4.getText());
        String host = this.jTextField2.getText();
        String username = this.jTextField1.getText();
        Socket socket;
        try{
            socket = new Socket(host,port);
            DataOutputStream send = new DataOutputStream(socket.getOutputStream());
            send.writeUTF("login:"+username);
            send.flush();
            DataInputStream receive = new DataInputStream(socket.getInputStream());
            String response = receive.readUTF();
            System.out.println(response);
            if(response.equals("Not exist")){
                JOptionPane.showMessageDialog(this, "You have not register yet");
                return;
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Fail to connect");
            return;
        }
        JOptionPane.showMessageDialog(this, "Connected");
        ClientMain Main = new ClientMain(new Client(username,clientport,socket),this);
        Main.setVisible(true);
        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

