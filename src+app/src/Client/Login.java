    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int port = Integer.parseInt(this.jTextField3.getText());
        int clientport = Integer.parseInt(this.jTextField4.getText());
        String host = this.jTextField2.getText();
        String username = this.jTextField1.getText();
        Socket socket;
        try{
            socket = new Socket(host,port);
            DataOutputStream send = new DataOutputStream(socket.getOutputStream());
            String ip = InetAddress.getLocalHost().getHostAddress();
            send.writeUTF("Reg:"+username+":"+clientport+":"+ip);
            send.flush();
            DataInputStream receive = new DataInputStream(socket.getInputStream());
            String response = receive.readUTF();
            System.out.println(response);
            if(response.equals("Duplicate name")){
                JOptionPane.showMessageDialog(this, "This username has registered");
                return;
            }
            JOptionPane.showMessageDialog(this,"Register success");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Fail to connect");
            return;
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

